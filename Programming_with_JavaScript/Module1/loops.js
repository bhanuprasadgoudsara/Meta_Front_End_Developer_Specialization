//for loop
for (var rec = 1; rec < 11; rec++) { 
    // var i=0; set a value
    // i<11;   specify a condition
    // i++;     increment the counter
    console.log(rec);
}

for (var i = 1; i <= 3; i = i+1) {
    console.log(i);
}
console.log('Go')

for (var i = 10; i > 0; i--) {
    // If you do not specify how the value of i should be increased after each loop, the for loop will run forever
    console.log(i);
}
console.log('Happy New Year!')

//while loop
var counter=12
while (counter < 31) {
    console.log(counter);
    counter = counter + 2;
}

var counter = 3
while (counter > 0) {
    console.log(counter);
    counter = counter - 1;
}
